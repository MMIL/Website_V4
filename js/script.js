let eventAdder = (booleanVal, el, btn) => {
  btn.addEventListener("click", () => {
    if (booleanVal) {
      el.style.display = "flex";
      el.scrollIntoView({ behavior: "smooth" });
      btn.textContent = "-";
    } else {
      el.style.display = "none";
      btn.textContent = "+";
    }
    booleanVal = !booleanVal;
  });
};
